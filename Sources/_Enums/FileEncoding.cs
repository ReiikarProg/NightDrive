namespace NightDrive.Enums
{
    public enum FileEncoding
    {
        /// <summary>
        /// Default value, used for Image files for example.
        /// </summary>
        None = 0,

        /// <summary>
        /// UTF-8 file, use by default.
        /// </summary>
        Utf8 = 1,

        /// <summary>
        /// Unicode file.
        /// </summary>
        Unicode = 2,

        /// <summary>
        /// ASCII file.
        /// </summary>
        Ascii = 3
    }
}
